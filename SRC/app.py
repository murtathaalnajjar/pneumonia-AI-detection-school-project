## clinic route
from ai_model import AI_pneumonia_check
@app.route('/clinic', methods=['GET', 'POST'])
def clinic():
    if request.method == "GET":
        data = db.fetch_list()
        return render_template('clinic.html', data=data)

    if request.method == "POST":
        name = request.form.get('name')
        age = request.form.get('age')
        symptoms = request.form.get('symptoms')
        img = request.files.get('xray') # hexadecimal format
        algorithm_result = AI_pneumonia_check(img)

        if img:
            img_data = img.read()
            db.add_record(name, age, symptoms, img_data, algorithm_result)
        
        return redirect('/clinic')

